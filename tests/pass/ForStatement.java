package pass;

public class ForStatement {
    public void compute() {
        int[] a = {2, 3, 4, 5};
        int b, c;
        int res = 0;
        
        for(int i = 0; i <= a.length-1; i++) {}

        for(int i = 0, k = 1; i <= a.length-1; i++) {
            res = k;
        }

        for(b = 0; b <= 3; b++) {}

        for(b = 0, c = 0; c <= 3 && b <= 3; b++) {}

        for(int i = 0, k = 1; i <= a.length-1; i++) {
            res = k;
        }
        

        //for(java.lang.Integer x = 1; res <= 2; res++) {}
        
        for(;;) {}
        
    }
}
